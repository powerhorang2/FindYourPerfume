package com.springriders.perfume.common.model;

public class PerfumeVO {
	private int i_p;
	private String p_nm;
	private int p_price;
	private String p_pic;
	private int p_brand;
	private int p_size;

	public int getI_p() {
		return i_p;
	}
	public void setI_p(int i_p) {
		this.i_p = i_p;
	}
	public String getP_nm() {
		return p_nm;
	}
	public void setP_nm(String p_nm) {
		this.p_nm = p_nm;
	}
	public int getP_price() {
		return p_price;
	}
	public void setP_price(int p_price) {
		this.p_price = p_price;
	}
	public String getP_pic() {
		return p_pic;
	}
	public void setP_pic(String p_pic) {
		this.p_pic = p_pic;
	}
	public int getP_brand() {
		return p_brand;
	}
	public void setP_brand(int p_brand) {
		this.p_brand = p_brand;
	}
	public int getP_size() {
		return p_size;
	}
	public void setP_size(int p_size) {
		this.p_size = p_size;
	}

}